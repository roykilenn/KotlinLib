package com.example.testlibrary

public class KotlinMethod {
    public fun Add(a:Int,b:Int): Int {
        return a+b;
    }
    public fun subtract(a:Int,b:Int): Int {
        return a-b;
    }
    public fun multiply(a:Int,b:Int): Int {
        return a*b;
    }
    public fun divide(a:Int,b:Int): Int {
        return a/b;
    }
}